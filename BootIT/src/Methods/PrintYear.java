package Methods;

public class PrintYear {

    public void printYear() {
        int year = 2025;
        int age = 24;

        System.out.println();
        System.out.println("The year is: " + year);

        year = year + 1;
        System.out.println("The next year will be " + year);

        System.out.println();
        System.out.println("My age is: " + age);

        age = age + 1;
        System.out.println("Next year I will be: " + age);

    }
}
